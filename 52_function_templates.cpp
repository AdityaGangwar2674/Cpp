#include <iostream>

using namespace std;
int max(int x, int y)
{
    return (x > y) ? x : y;
}
double max(double x, double y)
{
    return (x > y) ? x : y;
}
char max(char x, char y)
{
    return (x > y) ? x : y;
}

int main()
{
    cout << max(1, 2) << '\n';
    cout << max(1.1, 2.1) << '\n';
    cout << max('1', '2') << '\n';
    return 0;
}